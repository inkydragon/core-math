/* Check worst cases in [2^-1022, 2^-104) from those in the binade
   2^-1022 <= |x| < 2^-1021. We checked with BaCSeL that for all
   binades 2^(e-1) <= |x| < 2^e, for -1020 <= e <= -104, the worst cases
   are exactly those from the binade [2^-1022, 2^-1021) scaled by the
   corresponding exponent with the given sign. */
static void
check_tiny (void)
{
  for (int i = 0; i < N_WORST; i++)
  {
    double x = T[i];
    for (int e = -1020; e <= -104; e++)
    {
      // check binade 2^(e-1) <= |x| < 2^e
      check (ldexp (x, e + 1021));
      check (ldexp (-x, e + 1021));
    }
  }
}

